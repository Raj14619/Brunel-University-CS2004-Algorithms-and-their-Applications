
public class LinearSummation {

	public static void main(String[] args) {
		
		
		
		System.out.println(LinearSum(5));
	
		
	
	}
	
	public static int LinearSum(int N) {
		
		   int answer = 0;

	        for (int i = 0; i <= N; i++) {
	        
	            answer = answer + i;
	        }
		
	        
		return answer;

		}
	


}
